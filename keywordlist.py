import random
rando_Ragne = random.randrange(1,10)
rando_Nit = random.randint(1,10)
rando_Loaft = float(rando_Nit)
rando_Slit = [rando_Ragne, rando_Nit, rando_Loaft]
randoMunber = random.choice(rando_Slit)
print (randoMunber)
print (rando_Ragne)
print (rando_Loaft)
print (rando_Nit)


